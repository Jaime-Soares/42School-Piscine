#include <stdlib.h>
#include <stdio.h>
#include <unistd.h>

void	ft_print_alphabet(void)
{
	char	count;

	count = 97;
	while (count >= 97 && count <= 122)
	{
		write(1, &count, 1);
		count++;
	}
}	

int main()
{
	ft_print_alphabet();
}
