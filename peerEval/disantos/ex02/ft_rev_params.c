#include <unistd.h>

int	main(int size, char **argv)
{
	int	c;
	int	s;

	s = size - 1;
	if (size > 1)
	{
		while (s > 0)
		{
			c = 0;
			while (argv[s][c] != '\0')
				c++;
			write(1, argv[s], c);
			write(1, "\n", 1);
			s--;
		}
	}
	return (0);
}
